//Modal 1

const openModalM1 = document.querySelector('.modalM1');
const modalM1 = document.querySelector('.modal__M1');
const closeModalM1 = document.querySelector('.modal__closeM1');

openModalM1.addEventListener('click', (e) =>{
    e.preventDefault();
    modalM1.classList.add('modal--show');
})

closeModalM1.addEventListener('click', (e) =>{
    e.preventDefault();
    modalM1.classList.remove('modal--show');
})

